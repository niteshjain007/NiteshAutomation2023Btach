package practice;

public class ForLoopDemo {

	public static void main(String[] args) {
		
		//start point
		// limiting condition (end condition)
		// statements to execute
		// increment or decrement
		
		//start point;limiting condition;increment or decrement
		for(int r=1; r<=5 ; r++)
		{
			System.out.println("qaTestology"); //statements to execute
		}
		
		
		for(int t = 9 ; t< 14 ; t++)
		{
			System.out.println(t/2);
		}
		
	}

}
