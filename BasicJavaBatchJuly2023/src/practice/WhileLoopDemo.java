package practice;

public class WhileLoopDemo {

	public static void main(String[] args) {
		
		int count=15;   // initialize
		
		while(count < 21)  // condition check , limiting condition
		{
			System.out.println("qaTestology");  // stmt
			count = count+1; // increment or decrement
		}

		System.out.println("count==" + count);
		
		
		int q = 20;
		
		while(q > 10)
		{
			System.out.println("q:"+q);
			q = q-1;
		}
		
		int z = 34;
		while(z<39)
		{
			System.out.println(z);
			z++;  // z = z+1
		}
		
		/////////////////////////////
		
		int w = 7;
		
		while(w < 39)
		{
			System.out.println(w - 2);   //7-2//
			w = w + 5;
		}
			
		
		
		
		
	}

}
